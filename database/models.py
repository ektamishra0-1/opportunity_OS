class Observation:
    def __init__(
        self,
        source,
        source_id,
        title,
        content,
        author,
        url,
        timestamp
    ):
        self.source = source
        self.source_id = source_id
        self.title = title
        self.content = content
        self.author = author
        self.url = url
        self.timestamp = timestamp